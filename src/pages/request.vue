  <h1>Request registration</h1>
  <h2>ISO Registration</h2> 
  <p>(see <a href="#qtreg">below</a> for QuickTime registration)</p>
  <p>
    In order to apply for an identifier to be registered, you must abide by
    the following terms established by ISO. (Note that RID stands for
    &quot;registered identifier&quot;, a code-point in this case).
  </p>
  <blockquote>
    <p><font size="-1">The party requesting a format identifier shall:</font></p>
    <ol>
      <li type="a"><font size="-1">
        apply using the Form and procedures supplied by the
        Registration Authority;
      </font></li>
      <li type="a"><font size="-1">
        include a description of the purpose of the registered identifier,
        and the required technical details as specified in the application form;
      </font></li>
      <li type="a"><font size="-1">
        provide contact information describing how a complete description
        can be obtained on a non-discriminatory basis;
      </font></li>
      <li type="a"><font size="-1">
        agree to institute the intended use of the granted RID within a
        reasonable time frame; and
      </font></li>
      <li type="a"><font size="-1">
        to maintain a permanent record of the application form and the
        notification received from the Registration Authority of a granted
        RID.</font></li>
    </ol>
  </blockquote>
  <p>
    To register a code-point with this registration authority, please send
    email to <em>mp4reg</em> at <em>group.apple.com</em> (we regret the lack
    of a hyperlink, and the &quot;@&quot; sign, but this is so that spammers'
    web crawlers will not find the address easily).
  </p>
  <p>
    The subject of the email <em>must</em> include the word &quot;mp4reg&quot;
    somewhere. <strong>Messages without &quot;mp4reg&quot; in their subject
    may be discarded as spam and never seen or acted on by a human.</strong>
  </p>
  <p>In the body of the email, please identify:</p>
  <ol>
    <li type="1">
      The name, address, and URL of the organization requesting the code-point.
    </li>
    <li type="1">
      The kind of code-point you wish to register (please choose from the set of registered types).
    </li>
    <li type="1">
      For all except object-type registrations, the suggested identifier
      (four-character code). Note that four-character codes use four 8-bit
      printable characters, usually from the first 128 Unicode characters
      (commonly thought of as plain ASCII), but at most from the first 256
      Unicode characters.
    </li>
    <li type="1">
      The specification in which this code-point is defined, if possible.
      A copy of the specification would be appreciated, as it enables the
      authority to understand the registration better. If you are requesting
      a 'codec' code-point, a reference to the definition of the coding system
      itself, if separate from the definition of its storage in these files,
      would also be appreciated.
    </li>
    <li type="1">
      A brief 'abstract' of the meaning of the code-point, perhaps ten to
      twenty words (see examples on this site).
    </li>
    <li type="1">
      Contact information for an authorized representative for the code-point,
      including:
    </li>
    <ol>
      <li type="1">Contact person's name, title, and orgaanization.</li>
      <li type="1">Contact email.</li>
      <li type="1">Contact telephone number, and fax number if available.</li>
      <li type="1">Contact physical address (street address), if applicable.</li>
    </ol>
    <li type="1">
      Date of definition or implementation (if known) or intended date
      (if in future).
    </li>
    <li type="1">
      Statement of an intention to apply (implement) the assigned code-point.
    </li>
  </ol>
  <p>
    Applications with insufficient information, detail, or documentation,
    may be returned by the registration authority for clarification. Please
    be as complete and clear as possible.
  </p>
  <p>
    Items 1 through 5 will be published on the authority's web site. The
    remaining items are kept for the authority's records only, and are not
    published.
  </p>
  <p>
    If your suggested code-point is already in use, or the authority considers
    that the suggested value is unsuitable or may be misleading, the authority
    may suggest an alternative value.
  </p>

